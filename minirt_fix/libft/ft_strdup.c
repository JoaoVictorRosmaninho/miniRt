#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*dst;

	if (!s1)
		return (NULL);
	i = 0;
	dst = malloc((ft_strlen(s1) + 1) * sizeof(char));
	if (!dst)
		return (NULL);
	while (s1[i])
	{
		dst[i] = s1[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
